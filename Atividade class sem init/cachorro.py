class Cachorro:
    def mostrar_raca(self,raca):
        print(f'A raça do cachorro é: {raca}')

    def mostrar_idade(self,idade):
        print(f'A idade do cachorro é: {idade}')

    def mostrar_raca_idade(self, raca, idade):
        print(f"Seu cachorro tem {idade} anos e é da raça {raca}")